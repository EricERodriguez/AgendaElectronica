    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed
        if(RegistroActual > 0)
        {
            RegistroActual--;
            MostrarRegistros(RegistroActual);
            txtIndice.setText("" + (Integer.parseInt(txtIndice.getText()) - 1));
        }
        else
        {
            RegistroActual = RegistrosTotales;
            MostrarRegistros(RegistroActual);
        }
    }//GEN-LAST:event_btnAtrasActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        txtEditable(true);
        btnNuevo.setEnabled(false);
        btnGuardar.setEnabled(true);
        txtIndice.setText("" + (Integer.parseInt(txtIndice.getText()) + 1));
        LimpiarTxt();
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        txtEditable(false);
        btnNuevo.setEnabled(true);
        btnGuardar.setEnabled(false);
        RegistrosTotales++;
        RegistroActual = RegistrosTotales;
        strNombre[RegistrosTotales] = txtNombre.getText();
        strTelefono[RegistrosTotales] = txtTelefono.getText();
        strDireccion[RegistrosTotales] = txtDireccion.getText();
        strNacimiento[RegistrosTotales] = txtNacimiento.getText();
        strDni[RegistrosTotales] = txtDni.getText();
        strApellido[RegistrosTotales] = txtApellido.getText();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        if(RegistroActual < RegistrosTotales)
        {
            RegistroActual++;
            MostrarRegistros(RegistroActual);
            txtIndice.setText("" + (Integer.parseInt(txtIndice.getText()) + 1));
        }
        else
        {
            RegistroActual = RegistrosTotales;
            MostrarRegistros(RegistroActual);
            
        }
    }//GEN-LAST:event_btnSiguienteActionPerformed

